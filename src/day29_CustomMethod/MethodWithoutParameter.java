package day29_CustomMethod;

public class MethodWithoutParameter {

    public static void main(String[] args) {

        for(int i = 10; i > 6; i--){
            System.out.print("Hello");
        }

        System.out.println();

        System.out.println("Cybertek");


        for(int i = 10; i > 6; i--){
            System.out.print("Hello");
        }

        System.out.println();

        System.out.println("Siggy");

        for(int i = 10; i > 6; i--){
            System.out.print("Hello");
        }

    }

}
