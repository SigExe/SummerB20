package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \n\tMy name is Cybertek");
        System.out.println("\tI am in Texas\n");
        System.out.println("\n\n\t\t\tI love programming");

        System.out.println("\\"); // This prints me one backslash
        System.out.println("/"); // This prints me one frontslash

        System.out.println("\\\\"); // This prints me two backslashes


        System.out.println("My name is \'Sergio Garcia\'"); // 'Sergio Garcia'
        System.out.println("'Sergio Garcia'");


    }

}
