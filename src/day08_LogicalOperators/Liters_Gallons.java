package day08_LogicalOperators;

public class Liters_Gallons {

    public static void main(String[] args) {

        double gallons = 20;
        double liters = gallons * 3.785;
        System.out.println(gallons + " are equal to " + liters + " liters");

        double liters2 = 200;
        double gallons2 = 200 / 3.785;
        System.out.println(liters2 + " Liters are equal to " + gallons2 +" Gallons");


    }



}
