package day18_ForLoop;

public class ForLoop {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("===============================");

        for(int i = 0; i < 10; i++){
            System.out.println("Hello World");
        }

        for(int n = 1; n <= 5;n++){
            System.out.println("Cybertek Batch 20");
        }

        System.out.println("==============================");

        for(int n =1; n <= 10; n++ ){
            System.out.print(n + " ");
        }

        for(int t = 100; t >= 1; t--){
            System.out.print(t + " ");
        }



    }


}
